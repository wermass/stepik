# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, 
# –∫–æ—Ç–æ—Ä–∞—è –≤—ã–≤–æ–¥–∏—Ç —Å–ª–æ–≤–æ, –∫–æ—Ç–æ—Ä–æ–µ –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è —Ä–µ–∂–µ –≤—Å–µ–≥–æ, –±–µ–∑ —É—á–µ—Ç–∞ —Ä–µ–≥–∏—Å—Ç—Ä–∞. 
# –ï—Å–ª–∏ —Ç–∞–∫–∏—Ö —Å–ª–æ–≤ –Ω–µ—Å–∫–æ–ª—å–∫–æ, –≤—ã–≤–µ–¥–∏—Ç–µ —Ç–æ, –∫–æ—Ç–æ—Ä–æ–µ –º–µ–Ω—å—à–µ –≤ –ª–µ–∫—Å–∏–∫–æ–≥—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–º –ø–æ—Ä—è–¥–∫–µ.

# –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞.

# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Å–ª–æ–≤–æ (–≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ), –≤—Å—Ç—Ä–µ—á–∞–µ–º–æ–µ —Ä–µ–∂–µ –≤—Å–µ–≥–æ.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –ü—Ä–æ–≥—Ä–∞–º–º–∞ –Ω–µ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å —á—É–≤—Å—Ç–≤–∏—Ç–µ–ª—å–Ω–æ–π –∫ —Ä–µ–≥–∏—Å—Ç—Ä—É, —Å–ª–æ–≤–∞ apple –∏ Apple –¥–æ–ª–∂–Ω–∞ –≤–æ—Å–ø—Ä–∏–Ω–∏–º–∞—Ç—å –∫–∞–∫ –æ–¥–∏–Ω–∞–∫–æ–≤—ã–µ.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –°–ª–æ–≤–æ ‚Äî –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å –±—É–∫–≤. –ö—Ä–æ–º–µ —Å–ª–æ–≤ –≤ —Ç–µ–∫—Å—Ç–µ –º–æ–≥—É—Ç –ø—Ä–∏—Å—É—Ç—Å—Ç–≤–æ–≤–∞—Ç—å 
# –ø—Ä–æ–±–µ–ª—ã –∏ –∑–Ω–∞–∫–∏ –ø—Ä–µ–ø–∏–Ω–∞–Ω–∏—è .,!?:;-, –∫–æ—Ç–æ—Ä—ã–µ –Ω—É–∂–Ω–æ –∏–≥–Ω–æ—Ä–∏—Ä–æ–≤–∞—Ç—å. –î—Ä—É–≥–∏—Ö —Å–∏–º–≤–æ–ª–æ–≤ –≤ —Ç–µ–∫—Å—Ç–µ –Ω–µ—Ç.

# –¢–µ—Å—Ç–æ–≤—ã–µ –¥–∞–Ω–Ω—ã–µ üü¢
# Sample Input 1:

# home sweet home
# Sample Output 1:

# sweet
# Sample Input 2:

# home sweet home sweet.
# Sample Output 2:

# home

n = input()  #  –≤–≤–æ–¥–∏—Ç—Å—è —Å—Ç—Ä–æ–∫–∞
a = ''  #  —Å–æ–∑–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ –±–µ–∑ –ª–∏—à–Ω–∏—Ö —Å–∏–º–≤–æ–ª–æ–≤
result = {}  #  —Å–ª–æ–≤–∞—Ä—å –¥–ª—è –ø–æ–¥—Å—á–µ—Ç–∞ –∫–æ–ª–ª–∏—á–µ—Å—Ç–≤–∞ –≤—Ö–æ–∂–¥–µ–Ω–∏–π —Å–ª–æ–≤–∞ –≤ —Å—Ç—Ä–æ–∫–µ
exit = {}  #  —Å–ª–æ–≤–∞—Ä—å, —á—Ç–æ –±—ã —Å–æ–±—Ä–∞—Ç—å –≤—Å–µ —Å–ª–æ–≤–∞, –≤—Å—Ç—Ä–µ—á–∞—é—â–∏–µ—Å—è –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ä–∞–∑
for i in n:  #  —Ü–∏–∫–ª, –¥–ª—è –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∏ –ª–∏—à–Ω–∏—Ö –∑–Ω–∞–∫–æ–≤
    if i in ',.!?-;:':  #  –µ—Å–ª–∏ —Å–∏–º–≤–æ–ª - –∑–Ω–∞–∫ –ø—Ä–æ–ø—É—Å–∫–∞–µ–º
        continue
    a += i  #  –µ—Å–ª–∏ –Ω–µ—Ç, –¥–æ–±–∞–≤–ª—è–µ–º –≤ —Å—Ç—Ä–æ–∫—É –∞
lists = a.lower().split()   #  —Å–æ–∑–¥–∞–µ–º —Å–ø–∏—Å–æ–∫, —Å –Ω–∏–∂–Ω–∏–º —Ä–µ–≥–∏—Å—Ç—Ä–æ–º –∏–∑ —Å—Ç—Ä–æ–∫–∏
for j in lists:  #  —Å–ø–∏—Å–æ–∫ –≤—ã–≤–æ–¥–∏–º –≤ —Å–ª–æ–≤–∞—Ä—å, —Å –ø–æ–¥—Å—á—ë—Ç–æ–º –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π —Å–ª–æ–≤–∞ –≤ —Å—Ç—Ä–æ–∫–µ 
    result.setdefault(j, 0)  #  –¥–æ–±–∞–≤–ª—è–µ–º –∫–ª—é—á, –∫–æ—Ç–æ—Ä—ã–π —Ä–∞–≤–µ–Ω —Å–ª–æ–≤—É
    result[j] = result.get(j, 0) +1  #  —Å—á–∏—Ç–∞–µ–º –≤—Ö–æ–∂–¥–µ–Ω–∏–µ
values = result.values()  #  –≤—ã–≤–æ–¥–∏–º —Å–ø–∏—Å–æ–∫ –∑–Ω–∞—á–µ–Ω–∏–π, –∫–æ—Ç–æ—Ä—ã–µ –æ–±–æ–∑–Ω–∞—á–∞—é—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π —Å–ª–æ–≤–∞ –≤ —Å—Ç—Ä–æ–∫–µ 
values_min = min(values)  #  –Ω–∞—Ö–æ–¥–∏–º –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π (—á–∏—Å–ª–æ)
for k in result:  #  —Ü–∏–∫–ª, –ø–µ—Ä–µ–±–∏—Ä–∞–µ–º —Å–ª–æ–≤–∞ –∏–∑ —Å–ª–æ–≤–∞—Ä—è result
    g = result[k]  #  –¥–ª—è –≤–∏–∑—É–∞–ª–∏–∑–∞—Ü–∏–∏ —Å–æ–∑–¥–∞–ª, —á—Ç–æ –±—ã –ø–æ–Ω–∏–º–∞—Ç—å —á–µ–º—É —Ä–∞–≤–µ–Ω result[k] (—Ä–∞–±–æ—Ç–∞–ª —á–µ—Ä–µ–∑ pythontutor.com)
    if values_min == result[k]:  #  –µ—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ —Å–ª–æ–≤–∞ —Ä–∞–≤–Ω–æ –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–º—É –∑–Ω–∞—á–µ–Ω–∏—é
        exit.setdefault(k)  #  –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ –≤ —Å–ª–æ–≤–∞—Ä—å exit 
    
print(min(exit))   #  –ø–µ—á–∞—Ç–∞–µ–º –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ —Å–ª–æ–≤–æ –≤ –ª–µ–∫—Å–∏–∫–æ–≥—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–º –ø–æ—Ä—è–¥–∫–µ